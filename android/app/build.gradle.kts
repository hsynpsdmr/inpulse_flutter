import java.util.Properties
import java.io.FileInputStream

// Release imzalama ayarları
val keystoreProperties = Properties()
val keystorePropertiesFile = rootProject.file("key.properties")
if (keystorePropertiesFile.exists()) {
    keystoreProperties.load(FileInputStream(keystorePropertiesFile))
}

android {
    namespace = "com.inpulse.follower.reports"
    compileSdk = flutter.compileSdkVersion
    ndkVersion = "27.0.12077973"

    compileOptions {
        sourceCompatibility = JavaVersion.VERSION_11
        targetCompatibility = JavaVersion.VERSION_11
    }

    kotlinOptions {
        jvmTarget = JavaVersion.VERSION_11.toString()
    }

    defaultConfig {
        applicationId = "com.inpulse.follower.reports"
        minSdk = 21 // ForegroundService için minimum 21
        targetSdk = flutter.targetSdkVersion
        versionCode = flutter.versionCode
        versionName = flutter.versionName
    }

    signingConfigs {
        create("release") {
            storeFile = file(keystoreProperties.getProperty("storeFile"))
            storePassword = keystoreProperties.getProperty("storePassword")
            keyAlias = keystoreProperties.getProperty("keyAlias")
            keyPassword = keystoreProperties.getProperty("keyPassword")
        }
    }

    buildTypes {
        release {
            signingConfig = signingConfigs.getByName("release")

            // Yayına özel: kodu küçült ve kaynakları optimize et
            isMinifyEnabled = true
            isShrinkResources = true

            // Proguard (gereksiz kodları temizler)
            proguardFiles(
                getDefaultProguardFile("proguard-android-optimize.txt"),
                "proguard-rules.pro"
            )
        }

        debug {
            // Geliştirme sırasında gereksiz optimizasyonlar kapalı
            signingConfig = signingConfigs.getByName("release") // Optional: Debug için de imzalama
            isMinifyEnabled = false
            isShrinkResources = false
        }
    }
}

